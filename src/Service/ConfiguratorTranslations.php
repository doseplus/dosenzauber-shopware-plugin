    public function for(string $localeCode): array
    {
        $code = strtolower($localeCode);
        if (str_starts_with($code, 'en')) return self::STRINGS['en'];
        if (str_starts_with($code, 'pl')) return self::STRINGS['pl'];
        if (str_starts_with($code, 'fr')) return self::STRINGS['fr'];
        // de-DE, de-AT, gsw-CH (Schweizerdeutsch) und alles andere → Deutsch
        // gsw wird explizit gemappt damit der Sprach-Match nicht versehentlich auf Default fällt
        return self::STRINGS['de'];
    }
